package com.yifei.model;

public class Student_contact_way {
	private String Student_id;
	private String Student_name;
	private String Student_dormitory;
	private String Student_number;
	private String Student_tel;
	private String Student_QQ;
	private String Student_email;
	public String getStudent_id() {
		return Student_id;
	}
	public void setStudent_id(String student_id) {
		Student_id = student_id;
	}
	public String getStudent_name() {
		return Student_name;
	}
	public void setStudent_name(String student_name) {
		Student_name = student_name;
	}
	public String getStudent_dormitory() {
		return Student_dormitory;
	}
	public void setStudent_dormitory(String student_dormitory) {
		Student_dormitory = student_dormitory;
	}
	public String getStudent_number() {
		return Student_number;
	}
	public void setStudent_number(String student_number) {
		Student_number = student_number;
	}
	public String getStudent_tel() {
		return Student_tel;
	}
	public void setStudent_tel(String student_tel) {
		Student_tel = student_tel;
	}
	public String getStudent_QQ() {
		return Student_QQ;
	}
	public void setStudent_QQ(String student_QQ) {
		Student_QQ = student_QQ;
	}
	public String getStudent_email() {
		return Student_email;
	}
	public void setStudent_email(String student_email) {
		Student_email = student_email;
	}
	

}
